    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
       String firstname = jFieldFornamn.getText();
       String lastName = jFieldEfternamn.getText();
       String phone = jFieldTelefon.getText();
       String mail = jFieldEmail.getText();
       String hatsize = jFieldHattStorlek.getText();
       String adress = jFieldAdress.getText();
       String postalNr = jFieldPostalNr.getText();
       String city = jFieldCity.getText();
        
        if(firstname.isEmpty() || lastName.isEmpty() || phone.isEmpty() || mail.isEmpty() || hatsize.isEmpty() || adress.isEmpty() || postalNr.isEmpty() || city.isEmpty()){
           JOptionPane.showMessageDialog(null, "Samtliga fält måste vara ifyllda.");
           
       }else{

        try{
        idb.update("UPDATE Customer SET FirstName = '" + firstname + 
                                      "', LastName = '" + lastName +
                                          "',Phone = '" + phone +
                                           "', Email = '" + mail + 
                                            "', HatSize = '" + hatsize +
                                               "', Address = '" + adress +
                                                   "', PostalNr = '" + postalNr +
                                                     "', City = '" + city +
                                                       "' WHERE CustomerID = " + id);
                    
        JOptionPane.showMessageDialog(null, "En kund har ändrats.");
        
        new ChangeCustomerInfo(username).setVisible(true);
        dispose();      
        
        }catch(InfException e){
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null, "Error. Är samtliga fält korrekt ifyllda?");

        }
       }
    }//GEN-LAST:event_btnUpdateActionPerformed

