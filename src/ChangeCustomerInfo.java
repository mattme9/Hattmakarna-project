    public void fillCustomerBox(){
        try{
        cFornamn = new ArrayList<>();
        cEfternamn = new ArrayList<>();
        cCustomerID = new ArrayList<>();
        cFornamn = idb.fetchColumn("SELECT FirstName FROM Customer");
        cEfternamn = idb.fetchColumn("SELECT LastName FROM Customer");
        cCustomerID = idb.fetchColumn("SELECT CustomerID FROM Customer");
        for(int i = 0; i < cFornamn.size(); i++){
            if(!cFornamn.get(i).equals("Anonym")){   
            jComboKunder.addItem(cFornamn.get(i) + " " + cEfternamn.get(i) + " - ID:" + cCustomerID.get(i));
            }
        }
        }catch(InfException e){
            System.out.println(e.getMessage());
        }
    }
            
